import fs from "node:fs";
import path from "node:path";

// Configuration
const roots = [
  { group: "web", root: "/home/justin/web-projects" },
  { group: "ai", root: "/mnt/c/ai_projects" },
];

const excludedNames = new Set(["AI_LLM_projects", "justin-portfolio", "character-settings", "node_modules", "dist"]);

// Templates
const DOCKERFILE_REACT = `# Generated by PortfolioOps
FROM node:20-alpine AS builder
WORKDIR /app
COPY package*.json ./
RUN npm ci
COPY . .
# Build with base path for proxying
ARG BASE_PATH="/"
ENV VITE_BASE_PATH=$BASE_PATH
# Try to build, ignoring errors if script name differs, assumes 'build' exists
RUN npm run build

FROM nginx:alpine
COPY --from=builder /app/dist /usr/share/nginx/html
# Custom Nginx config to handle SPA routing under a subdirectory
RUN echo 'server { \
    listen 80; \
    location / { \
        root /usr/share/nginx/html; \
        index index.html index.htm; \
        try_files $uri $uri/ /index.html; \
    } \
}' > /etc/nginx/conf.d/default.conf
EXPOSE 80
CMD ["nginx", "-g", "daemon off;"]
`;

const DOCKERFILE_PYTHON = `# Generated by PortfolioOps
FROM python:3.10-slim
WORKDIR /app

# Install system dependencies if needed (e.g. for cv2)
RUN apt-get update && apt-get install -y --no-install-recommends \
    libgl1-mesa-glx \
    libglib2.0-0 \
    && rm -rf /var/lib/apt/lists/*

COPY requirements.txt .
RUN pip install --no-cache-dir -r requirements.txt

COPY . .

# Default to 8000, can be overridden
ENV PORT=80
EXPOSE 80

# Assumes app.py or main.py exists
CMD ["python", "app.py"]
`;

// Detection Logic
function detectType(dir) {
  try {
    if (fs.existsSync(path.join(dir, "package.json"))) {
      const content = fs.readFileSync(path.join(dir, "package.json"), "utf8");
      if (!content.trim()) return "unknown"; // Empty file
      const pkg = JSON.parse(content);
      if (pkg.dependencies?.react || pkg.devDependencies?.vite) return "react";
      return "node"; 
    }
  } catch (e) {
    console.warn(`[${path.basename(dir)}] Failed to parse package.json: ${e.message}`);
    return "unknown";
  }

  if (fs.existsSync(path.join(dir, "requirements.txt")) || fs.existsSync(path.join(dir, "pyproject.toml"))) {
    return "python";
  }
  return "unknown";
}

function findPythonEntry(dir) {
    const candidates = ["app.py", "main.py", "server.py", "ui.py", "gradio_app.py", "run.py", "index.py"];
    for (const c of candidates) {
        if (fs.existsSync(path.join(dir, c))) return c;
    }
    // Fallback: find any non-init python file? risky but better than nothing for "demo"
    return null;
}

function main() {
  let createdCount = 0;

  for (const { group, root } of roots) {
    if (!fs.existsSync(root)) continue;
    
    const dirs = fs.readdirSync(root, { withFileTypes: true });
    
    for (const dir of dirs) {
      if (!dir.isDirectory() || dir.name.startsWith(".") || excludedNames.has(dir.name)) continue;
      
      const projectPath = path.join(root, dir.name);
      const dockerfilePath = path.join(projectPath, "Dockerfile");
      
      // If Dockerfile exists, skip (respect custom configs)
      if (fs.existsSync(dockerfilePath)) continue;

      const type = detectType(projectPath);
      
      if (type === "react") {
        console.log(`[${dir.name}] Detected React/Vite. Creating Dockerfile...`);
        fs.writeFileSync(dockerfilePath, DOCKERFILE_REACT);
        createdCount++;
      } else if (type === "python") {
        console.log(`[${dir.name}] Detected Python. Creating Dockerfile...`);
        const entry = findPythonEntry(projectPath);
        
        if (entry) {
            const content = DOCKERFILE_PYTHON.replace('CMD ["python", "app.py"]', `CMD ["python", "${entry}"]`);
            fs.writeFileSync(dockerfilePath, content);
            createdCount++;
        } else {
             console.warn(`[${dir.name}] Python project found but no obvious entry point. Skipping.`);
        }
      } else {
        // console.warn(`[${dir.name}] Unknown type. Skipping.`);
      }
    }
  }
  
  console.log(`Ops Init: Generated ${createdCount} new Dockerfiles.`);
}

main();
